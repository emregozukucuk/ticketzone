CREATE DATABASE IF NOT EXISTS ticketzone;
use ticketzone;
CREATE TABLE IF NOT EXISTS bus (
  bus_id INT NOT NULL AUTO_INCREMENT,
  departure VARCHAR(255),
  arrival VARCHAR(255),
  departure_time TIME,
  arrival_time TIME,
  departure_date DATE,
  arrival_date DATE,
  PRIMARY KEY (bus_id)
);
CREATE TABLE IF NOT EXISTS seat (
  seat_number INT NOT NULL,
  bus_id INT NOT NULL,
  price DOUBLE,
  status CHAR,
  INDEX (bus_id),
  PRIMARY KEY (bus_id, seat_number),
  FOREIGN KEY (bus_id) REFERENCES bus(bus_id)
  ON DELETE CASCADE
  ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS reservation (
  reservation_no INT NOT NULL AUTO_INCREMENT,
  bus_id INT NOT NULL,
  seat_number INT NOT NULL,
  customer_name VARCHAR(255) NOT NULL,
  INDEX (bus_id, seat_number),
  UNIQUE (bus_id, seat_number),
  PRIMARY KEY (reservation_no, bus_id, seat_number),
  FOREIGN KEY (bus_id, seat_number) REFERENCES seat(bus_id, seat_number)
  ON DELETE CASCADE
  ON UPDATE CASCADE
);
